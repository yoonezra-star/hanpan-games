function relatedFor(game) {
  const same = catalog.filter((item) => item.id !== game.id && item.category === game.category).slice(0, 3);
  return same.length >= 3 ? same : catalog.filter((item) => item.id !== game.id).slice(0, 3);
}

function listHtml(items, className = "") {
  const classAttr = className ? ` class="${className}"` : "";
  return `<ul${classAttr}>
            ${items.map((item) => `<li>${htmlEscape(item)}</li>`).join("\n            ")}
          </ul>`;
}

function snapshotHtml(items) {
  return `<div class="detail-snapshot" aria-label="게임 요약">
            ${items.map(([label, text]) => `<div><strong>${htmlEscape(label)}</strong><span>${htmlEscape(text)}</span></div>`).join("\n            ")}
          </div>`;
}

