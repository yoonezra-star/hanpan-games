function htmlEscape(value) {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function hasFinalConsonant(value) {
  const chars = [...value.trim()].reverse();
  const korean = chars.find((char) => {
    const code = char.codePointAt(0);
    return code >= 0xac00 && code <= 0xd7a3;
  });
  if (!korean) return false;
  return (korean.codePointAt(0) - 0xac00) % 28 !== 0;
}

function topicName(value) {
  return `${value}${hasFinalConsonant(value) ? "은" : "는"}`;
}

function withName(value) {
  return `${value}${hasFinalConsonant(value) ? "과" : "와"}`;
}

